#include <iomanip>

#include "Item.h"

//------------------------------------------------------------------------------
Item::Item()
    :Item(0, "Air")
{
}

//------------------------------------------------------------------------------
Item::Item(int id)
    :Item(id, "Air")
{
}

//------------------------------------------------------------------------------
Item::Item(int id, std::string name)
    :name(name)
{
    this->id   = id;
}

//------------------------------------------------------------------------------
void Item::display(std::ostream &outs) const
{
    outs << std::right << std::setw(3) << id << " " << name;
}
